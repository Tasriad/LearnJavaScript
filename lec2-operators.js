// Arithmetic Operators 
let a = 10;
let b = 4;
console.log("a = ",a + ", b = ",b)
console.log("a + b = ", a + b)
console.log("a - b = ", a - b)
console.log("a / b = ", a / b)
console.log("a ** b = ", a ** b)
console.log("a % b = ", a % b)
console.log("++a = ", ++a)
console.log("a++ = ", a++)
console.log("--a = ", --a)
console.log("a-- = ", a--)
console.log("a = ", a)
console.log("a-- = ", a--)

// Assignment Operators 
let assignment = 1;
assignment += 5 // same as assignment = assignment + 5
console.log("a is now = ", assignment)
assignment = 1;
assignment -= 5 // same as assignment = assignment - 5
console.log("a is now = ", assignment)
assignment = 1;
assignment *= 5 // same as assignment = assignment * 5
console.log("a is now = ", assignment)
assignment = 1;
assignment /= 5 // same as assignment = assignment / 5
console.log("a is now = ", assignment)
assignment = 1;
assignment **=5;// same as assignment = assignment ** 5
console.log("a is now = ", assignment)

// Comparison Operators 
let comp1 = 6;
let comp2 = "6";
